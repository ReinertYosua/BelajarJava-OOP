package Associaton;

public class Mahasiswa {
	private String nim;
	private String nama;
	private String jalanAlamat;
	private String kotaAlamat;
	
	public String getNim() {
		return nim;
	}
	public void setNim(String nim) {
		this.nim = nim;
	}
	public String getNama() {
		return nama;
	}
	public void setNama(String nama) {
		this.nama = nama;
	}
	public String getJalanAlamat() {
		return jalanAlamat;
	}
	public void setJalanAlamat(String jalanAlamat) {
		this.jalanAlamat = jalanAlamat;
	}
	public String getKotaAlamat() {
		return kotaAlamat;
	}
	public void setKotaAlamat(String kotaAlamat) {
		this.kotaAlamat = kotaAlamat;
	}
	
	
	
	
}
